package capsule.human;

public class HumanCapsule1 {
	 private String name;
	    private int age;

	    public HumanCapsule1(String name, int age) {
	        this.name = name;
	        this.age = age;
	    }

	    public String getName() {
	        return this.name;
	    }

	    public void setName(String name) {
	        this.name = name;
	    }

	    public int getAge() {
	        return this.age;
	    }

}
